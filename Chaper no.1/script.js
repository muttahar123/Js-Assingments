function showCheckboxAlert() {
    // Display a confirm dialog with a checkbox
    var isConfirmed = window.confirm("Do you agree to the terms and conditions?");

    // Check if the user clicked OK or Cancel
    if (isConfirmed) {
        alert("You agreed!");
    } else {
        alert("You did not agree.");
    }
}

// Call the function to show the checkbox alert
showCheckboxAlert();


